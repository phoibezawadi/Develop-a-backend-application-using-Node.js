function calculate(operation, num1, num2) {
  switch (operation) {
    case 'addition':
      return num1 + num2;
    case 'subtraction':
      return num1 - num2;
    case 'multiplication':
      return num1 * num2;
    default:
      return 'Invalid operation';
  }
}







